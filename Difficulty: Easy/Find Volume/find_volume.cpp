// Function to return the volume of a cube
int volume(int s) {
    return s * s * s;  // Volume of cube = side^3
}

// Function to return the volume of a rectangular box
int volume(int l, int w, int h) {
    return l * w * h;  // Volume of rectangular box = length * width * height
}
